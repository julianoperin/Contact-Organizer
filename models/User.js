const mongoose = require("mongoose");

//! USER SCHEMA
const UserSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

//! Export User
module.exports = mongoose.model("User", UserSchema);
